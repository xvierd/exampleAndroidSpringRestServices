public class Cartelera {

    int id;
    int carteleraImagen;
    String textoCartelera;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCarteleraImagen() {
        return carteleraImagen;
    }

    public void setCarteleraImagen(int carteleraImagen) {
        this.carteleraImagen = carteleraImagen;
    }

    public String getTextoCartelera() {
        return textoCartelera;
    }

    public void setTextoCartelera(String textoCartelera) {
        this.textoCartelera = textoCartelera;
    }
}
